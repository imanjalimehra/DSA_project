import os
import sys
from collections import defaultdict

# -------------------- Trie Node Definition -------------------- #
class TrieNode:
    def __init__(self):
        self.children = defaultdict(TrieNode)
        self.is_end = False

# -------------------- Trie Class -------------------- #
class Trie:
    def __init__(self):
        self.root = TrieNode()

    def insert(self, word: str):
        node = self.root
        for char in word.lower():
            node = node.children[char]
        node.is_end = True

    def search(self, word: str) -> bool:
        node = self.root
        for char in word.lower():
            if char not in node.children:
                return False
            node = node.children[char]
        return node.is_end

    def suggestions(self, prefix: str) -> list:
        results = []

        def dfs(node: TrieNode, path: str):
            if node.is_end:
                results.append(path)
            for char, child in node.children.items():
                dfs(child, path + char)

        node = self.root
        for char in prefix.lower():
            if char not in node.children:
                return []
            node = node.children[char]

        dfs(node, prefix.lower())
        return results[:10]

# -------------------- Levenshtein Distance -------------------- #
def edit_distance(word1: str, word2: str) -> int:
    n, m = len(word1), len(word2)
    dp = [[0]*(m+1) for _ in range(n+1)]

    for i in range(n+1):
        dp[i][0] = i
    for j in range(m+1):
        dp[0][j] = j

    for i in range(1, n+1):
        for j in range(1, m+1):
            if word1[i-1] == word2[j-1]:
                dp[i][j] = dp[i-1][j-1]
            else:
                dp[i][j] = 1 + min(
                    dp[i-1][j],     # delete
                    dp[i][j-1],     # insert
                    dp[i-1][j-1]    # replace
                )
    return dp[n][m]

def spell_check(input_word: str, dictionary: list) -> list:
    distances = [(word, edit_distance(input_word.lower(), word)) for word in dictionary]
    distances.sort(key=lambda x: x[1])
    return [word for word, _ in distances[:5]]

# -------------------- Dictionary Loader -------------------- #
def load_words(file_path: str, trie: Trie) -> list:
    words = []
    try:
        with open(file_path, 'r') as f:
            for line in f:
                word = line.strip()
                if word:
                    words.append(word)
                    trie.insert(word)
    except FileNotFoundError:
        print(f"âŒ Oops! The dictionary file '{file_path}' was not found. Please check the path.")
        sys.exit(1)
    return words

